const { app, BrowserWindow, Menu } = require('electron');
const path = require('path');

// Hold en global referanse til vinduet
let mainWindow;

function createWindow() {
  // Opprett browser vinduet
  mainWindow = new BrowserWindow({
    width: 1200,
    height: 800,
    minWidth: 800,
    minHeight: 600,
    webPreferences: {
      nodeIntegration: false,
      contextIsolation: true,
      enableRemoteModule: false,
      webSecurity: true
    },
    icon: path.join(__dirname, 'public/icon-512x512.png'), // Hvis du har ikon
    titleBarStyle: 'hiddenInset', // Mac-stil title bar
    show: false // Ikke vis før den er klar
  });

  // Last inn din deployed Vercel URL eller localhost
  const isDev = process.env.NODE_ENV === 'development';
  
  if (isDev) {
    mainWindow.loadURL('http://localhost:3000');
    mainWindow.webContents.openDevTools();
  } else {
    // Endre til din Vercel URL når du deployer
    mainWindow.loadURL('https://djsportsweb.vercel.app');
  }

  // Vis vinduet når det er klart
  mainWindow.once('ready-to-show', () => {
    mainWindow.show();
  });

  // Lukk appen når alle vinduer er lukket
  mainWindow.on('closed', () => {
    mainWindow = null;
  });
}

// Mac-spesifikke menyer
const template = [
  {
    label: 'djSports',
    submenu: [
      { role: 'about' },
      { type: 'separator' },
      { role: 'services' },
      { type: 'separator' },
      { role: 'hide' },
      { role: 'hideothers' },
      { role: 'unhide' },
      { type: 'separator' },
      { role: 'quit' }
    ]
  },
  {
    label: 'Edit',
    submenu: [
      { role: 'undo' },
      { role: 'redo' },
      { type: 'separator' },
      { role: 'cut' },
      { role: 'copy' },
      { role: 'paste' }
    ]
  },
  {
    label: 'View',
    submenu: [
      { role: 'reload' },
      { role: 'forceReload' },
      { role: 'toggleDevTools' },
      { type: 'separator' },
      { role: 'resetZoom' },
      { role: 'zoomIn' },
      { role: 'zoomOut' },
      { type: 'separator' },
      { role: 'togglefullscreen' }
    ]
  },
  {
    label: 'Window',
    submenu: [
      { role: 'minimize' },
      { role: 'close' }
    ]
  }
];

const menu = Menu.buildFromTemplate(template);
Menu.setApplicationMenu(menu);

// App event listeners
app.whenReady().then(createWindow);

app.on('window-all-closed', () => {
  if (process.platform !== 'darwin') {
    app.quit();
  }
});

app.on('activate', () => {
  if (BrowserWindow.getAllWindows().length === 0) {
    createWindow();
  }
});